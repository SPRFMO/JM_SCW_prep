save_ridge_plot <- function(plot, basename, width = 10, height = 11.5) {
  for (out_dir in c(file.path("doc", "images"), file.path("docs", "images"))) {
    dir.create(out_dir, showWarnings = FALSE, recursive = TRUE)
    ggsave(
      filename = file.path(out_dir, paste0(basename, ".png")),
      plot = plot,
      width = width,
      height = height,
      dpi = 300,
      bg = "white"
    )

    svg_file <- file.path(out_dir, paste0(basename, ".svg"))
    grDevices::svg(filename = svg_file, width = width, height = height, bg = "white")
    print(plot)
    grDevices::dev.off()
  }
}

